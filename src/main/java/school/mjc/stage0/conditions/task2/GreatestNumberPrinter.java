package school.mjc.stage0.conditions.task2;

public class GreatestNumberPrinter {
    public void printGreatest(int first, int second) {
        if(first > second){
            System.out.print(first);
        } else if (first == second) {
            System.out.print(second);
        }else{
            System.out.print(second);
        }
    }
}
